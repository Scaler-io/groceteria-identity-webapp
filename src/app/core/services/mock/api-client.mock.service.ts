@Injectable({ providedIn: 'root' })
export class ApiClientMockService {
  constructor() {}

  public getApiClientPaginatedData(
    pageIndex: number = 1,
    pageSize: number = 20
  ): Observable<PaginatedApiClientList> {
    const total = 43;
    const start = pageSize * (pageIndex - 1);
    const end = 43 - start >= 20 ? start + 1 + pageSize - 1 : total + 1;

    let data = [...apiClientData];
    let temp = data.slice(start, end);

    return of(<PaginatedApiClientList>{
      count: temp.length,
      pageIndex: pageIndex,
      pageSize: pageSize,
      data: temp,
    });
  }

  public getTotalApiClientCount(): Observable<number> {
    return of(apiClientData.length);
  }

  public getApiClientDetails(clientId: string): Observable<ApiClient> {
    const temp = sampleApiClient;
    temp.clientId = clientId;
    return of(temp);
  }
}

